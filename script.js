let button = document.querySelector(".ipl-btn button");
let heading = document.querySelector("h1");
let trophyWrapper = document.querySelector(".trophy-details");
let section = document.querySelector("section");
let iplTeam = document.querySelector(".ipl-team");
let captainImg = document.querySelector(".captain-img");
let captainLogo = document.querySelector(".captain-logo");

function renderTeam(team) {
  heading.style.color = team.secondaryColor;
  button.style.backgroundColor = team.secondaryColor;
  iplTeam.style.backgroundColor = team.secondaryColor;
  heading.innerText = team.teamName;
  
  trophyWrapper.innerHTML = "";
  iplTeam.innerHTML = "";

  if (team.trophies.length > 1) {
    trophyWrapper.style.gridTemplateColumns = "1fr 1fr";
  } else {
    trophyWrapper.style.gridTemplateColumns = "1fr";
  }

  section.style.backgroundColor = team.primaryColor;

  team.trophies.forEach(val => {
    let divCreate = document.createElement("div");
    let imageCreate = document.createElement("img");
    let h5Create = document.createElement("h5");

    imageCreate.src = val.img;
    h5Create.innerText = val.year;

    divCreate.classList.add("ipl-trophy");
    h5Create.classList.add("trophy-year");

    divCreate.appendChild(imageCreate);
    divCreate.appendChild(h5Create);

    trophyWrapper.style.background = team.secondaryColor;
    trophyWrapper.appendChild(divCreate);
  });

  team.teamMembers2025.forEach(val => {
    let memberName = document.createElement("h5");
    memberName.classList.add("ipl-name");

    memberName.innerText = val;

    iplTeam.appendChild(memberName);
  });

  let captainimg = document.createElement("img");
  captainimg.setAttribute("src", team.captainImg);
  captainimg.setAttribute("alt", "captain-img");

  captainImg.innerHTML = "";
  captainImg.appendChild(captainimg);

  let captainlogo = document.createElement("img");
  captainlogo.setAttribute("src", team.teamLogo);
  captainlogo.setAttribute("alt", "teamLogo-img");
  captainlogo.classList.add("logo");

  captainLogo.innerHTML = "";
  captainLogo.appendChild(captainlogo);
}

renderTeam(iplTeams[0]);

button.addEventListener("click", () => {
  let randomTeam = iplTeams[Math.floor(Math.random() * iplTeams.length)];
  renderTeam(randomTeam);
});
